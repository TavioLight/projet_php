        <h1 class="text-center">Le Concours</h1>
        <p>l'Institut Africain d'd’Informatique organise chaque année un concours d'admission pour les étudiants en Licence.</p>

        <h2>Dates et lieu du concours</h2>
        <p>Le concours se déroule généralement au mois de juillet-Aout . Les épreuves écrites se déroulent sur le campus de l'Université .</p>

        <h2>Modalités d'inscription</h2>
        <p>Les inscriptions au concours se font en ligne sur le site web de l'université. La date limite d'inscription est généralement fixée au mois de juillet. Les frais d'inscription s'élèvent à 10 000fcfa .</p>

        <h2>Convocation</h2>
        <p>Les convocations au concours sont envoyées par email aux candidats inscrits. La convocation mentionne la date, l'heure et le lieu des épreuves.</p>

        <h2>Conditions d'admission</h2>
        <p>Pour être admissible au concours, les candidats doivent être titulaires d'un baccalauréat ou d'un diplôme équivalent. Le dossier de candidature doit comporter le relevé de notes du baccalauréat, une lettre de motivation et un CV. Les candidats sont sélectionnés sur la base de leur dossier scolaire et de leur performance au concours.</p>

        <h2>Déroulement du concours</h2>
        <p>Le concours d'admission en Licence se compose de trois épreuves écrites:</p>
        <ul>
            <li>Une épreuve de français</li>
            <li>Une épreuve de mathématiques</li>
            <li>Une épreuve de d'anglais </li>
        </ul>
