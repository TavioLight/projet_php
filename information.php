        <p>Les coefficients des épreuves sont également variables. Le barème de notation est publié sur le site web de l'université. Le matériel à apporter le jour du concours est indiqué sur la convocation.</p>

        <h2>Conseils aux candidats</h2>
        <p>Pour bien se préparer au concours, les candidats peuvent:</p>
        <ul>
            <li>Réviser les programmes du baccalauréat</li>
            <li>S'entraîner aux épreuves écrites</li>
            <li>Préparer un argumentaire pour l'entretien de motivation</li>
        </ul>
        <p>Le jour du concours, les candidats doivent:</p>
        <ul>
            <li>Arriver en avance minimum 1heure</li>
            <li>Se munir de leur convocation et d'une pièce d'identité</li>
            <li>Rester calmes et concentrés</li>
        </ul>

